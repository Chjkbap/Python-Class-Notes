""" Create a list """
list1 = [10, 20, 15, 12, 200] # assign and initialise the list
print(type(list1)) # returns the list type class
print(list1)


"""convert list into bytes"""
byteVal = bytes(list1)
print(type(byteVal))
print(byteVal)

""" convert list into bytearray"""
byteArrayval = bytearray(list1)
print(type(byteArrayval))
print(byteArrayval)